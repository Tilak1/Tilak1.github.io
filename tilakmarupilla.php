                <h3><a name="biography">Biography</a></h3>

                <p class="text-content">
                  Ali Saeizadeh is a Ph.D. student at 
                  <a href="https://www.northeastern.edu/wiot">Institute for the Wireless Internet of Things</a> 
                  at Northeastern University, USA, under Prof. <a href="https://ece.northeastern.edu/wineslab/tmelodia.php">Tommaso Melodia</a>.
                  In 2022, he obtained his Bachelor's degree in Electrical Engineering at the 
                  <a href="https://ece.ut.ac.ir/en">University of Tehran</a>.
                  His main research efforts are focused on the application of AI and machine learning in wireless communication, digital twin technology, embedded deep learning applications, and next-generation wireless communication systems.
                </p>



